// ---------------------------------------------------------------------------
// glyph-registry.mjs — ONE source of truth for reader glyph sets, certified
// family read commands (pools), and per-consumer rosters.
//
// Adding a new template set = ONE edit here:
//   1. a SETS line (name → npz in assets/fonts/) — export-glyphs rebuilds
//      glyphs.bin from this manifest (`node tools/export-glyphs.mjs`, then
//      `--check`);
//   2. if it belongs to a proven producer family, extend/add the POOLS entry
//      (the certified --glyphs string + tol/palette flags — copy the exact
//      command the family proof used);
//   3. if batch-read should probe it, list the pool in BATCH_LADDER;
//   4. if the browser app should load it by default, add it to APP_ROSTER
//      AND to DEFAULT_SETS in src/blindocr.js (the app is a browser file and
//      cannot import this module — `npm test` fails if the two lists drift).
//
// Consumers: tools/export-glyphs.mjs (SETS), tools/gate.mjs (POOLS),
// tools/batch-read.mjs (POOLS + BATCH_LADDER), test/test.js (drift checks).
// The ocr/ renderer-identification lab intentionally does NOT import this
// file — it stays self-contained with its own families.mjs registry; when a
// hunt closes, its found config graduates into fontgen sets + a line here
// (integration recipe: ocr/README.md).
// ---------------------------------------------------------------------------

